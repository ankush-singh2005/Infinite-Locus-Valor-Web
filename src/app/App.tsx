// ─── App ──────────────────────────────────────────────────────────────────────

export default function App() {
  const [page, setPage] = useState<Page>("landing");
  const [lecturesBackPage, setLecturesBackPage] = useState<Page>("home");

  // Navigate to lectures/seminars page
  const goLectures = (from: Page) => {
    setLecturesBackPage(from);
    setPage("lectures-seminars");
  };

  const goHome = () => setPage("home");
  const goSessions = () => setPage("sessions");
  const goRequests = () => setPage("requests");
  const goSessionDetail = () => setPage("session-detail");
  const goCreatePackage = () => setPage("create-package");
  const goMyPackages = () => setPage("my-packages");

  const sidebarPage: Page = page === "session-detail" ? "sessions"
    : page === "requests" ? "home"
    : page === "create-package" ? "my-packages"
    : page;

  if (page === "landing") {
    return <LandingPage onEnter={() => setPage("onboarding")} />;
  }

  if (page === "onboarding") {
    return <LanguageSelectPage onBack={() => setPage("landing")} onContinue={() => setPage("account-setup")} />;
  }

  if (page === "account-setup") {
    return <AccountSetupPage onBack={() => setPage("onboarding")} onSubmit={() => setPage("choose-path")} />;
  }

  if (page === "choose-path") {
    return <ChoosePathPage onBack={() => setPage("account-setup")} onContinue={(path) => setPage(path === "CLUB" ? "club-home" : "select-tier")} />;
  }

  if (page === "select-tier") {
    return <SelectTierPage onBack={() => setPage("choose-path")} onContinue={() => setPage("define-craft")} />;
  }

  if (page === "define-craft") {
    return <DefineCraftPage onBack={() => setPage("select-tier")} onContinue={() => setPage("choose-focus")} />;
  }

  if (page === "choose-focus") {
    return <ChooseFocusPage onBack={() => setPage("define-craft")} onContinue={goHome} />;
  }

  if (page === "club-home") {
    return <ClubDashboardPage onFindCoach={() => setPage("club-coaches")} onShortlisted={() => setPage("shortlisted-coaches")} onSessions={() => setPage("club-sessions")} onManageSquads={() => setPage("manage-squads")} onLectures={() => goLectures("club-home")} />;
  }

  if (page === "club-coaches") {
    return <ClubCoachesPage onBack={() => setPage("club-home")} onFilters={() => setPage("club-filters")} onViewCoach={() => setPage("coach-profile")} />;
  }

  if (page === "club-filters") {
    return <FiltersPage onBack={() => setPage("club-coaches")} onApply={() => setPage("club-coaches")} />;
  }

  if (page === "coach-profile") {
    return <CoachProfilePage onBack={() => setPage("club-coaches")} onBook={() => setPage("book-coach")} />;
  }

  if (page === "book-coach") {
    return <BookCoachPage onBack={() => setPage("coach-profile")} onPay={() => setPage("package-deals")} />;
  }

  if (page === "payment") {
    return <PaymentPage onBack={() => setPage("book-coach")} />;
  }

  if (page === "shortlisted-coaches") {
    return <ShortlistedCoachesPage onBack={() => setPage("club-home")} onViewCoach={() => setPage("coach-profile")} />;
  }

  if (page === "club-sessions") {
    return <SessionsPage onBack={() => setPage("club-home")} />;
  }

  if (page === "manage-squads") {
    return <ManageSquadsPage onBack={() => setPage("club-home")} />;
  }

  if (page === "package-deals") {
    return <PackageDealsPage onBack={() => setPage("book-coach")} onPay={() => setPage("payment")} />;
  }

  return (
    <div className="flex h-screen bg-[#050505] text-white overflow-hidden font-['Inter',sans-serif]">
      <Sidebar
        activePage={sidebarPage}
        setPage={(p) => {
          if (p === "sessions") goSessions();
          else if (p === "my-packages") goMyPackages();
          else if (p === "profile") setPage("profile");
          else if (p === "content-hub") setPage("content-hub");
          else if (p === "learn") setPage("learn");
          else goHome();
        }}
      />
      {page === "home" && <HomePage goRequests={goRequests} goSessions={goSessions} goSessionDetail={goSessionDetail} goLectures={() => goLectures("home")} goCreatePackage={goCreatePackage} />}
      {page === "sessions" && <AvailabilityPage onBack={goHome} />}
      {page === "requests" && <RequestsPage onBack={goHome} />}
      {page === "session-detail" && <SessionDetailPage onBack={goSessions} />}
      {page === "my-packages" && <MyPackagesPage onCreateNew={goCreatePackage} onEditPackage={goCreatePackage} />}
      {page === "create-package" && <CreatePackagePage onBack={goMyPackages} />}
      {page === "profile" && <ProfilePage onCredentials={() => setPage("credentials")} onCoachTier={() => setPage("coach-tier")} onFinancials={() => setPage("financials")} />}
      {page === "credentials" && <CredentialsPage onBack={() => setPage("profile")} />}
      {page === "coach-tier" && <CoachTierPage onBack={() => setPage("profile")} />}
      {page === "content-hub" && <ContentHubPage onBack={goHome} />}
      {page === "financials" && <FinancialsPage onBack={() => setPage("profile")} />}
      {page === "learn" && <LearnPage onSelect={() => setPage("lecture-detail")} />}
      {page === "lecture-detail" && <LectureDetailPage onBack={() => setPage("learn")} />}
      {page === "lectures-seminars" && <LecturesSeminarsAllPage onBack={() => setPage(lecturesBackPage)} onSelect={() => setPage("lecture-detail")} />}
    </div>
  );
}

